// 1. Изменить режим темы (темная/светлая)
import { theme } from '$lib/stores/themeStore';
theme.setTheme('light');

// 2. Переключить режим (темная ↔ светлая)
theme.toggleTheme();

// 3. Изменить палитру цветов
theme.setPalette('palette2');

// 4. Подписаться на изменения
theme.subscribe((state) => {
  console.log(state.mode);      // 'dark' | 'light'
  console.log(state.palette);   // 'palette1' | 'palette2' | ...
});

