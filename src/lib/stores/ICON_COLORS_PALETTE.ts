/**
 * Получить оптимальный цвет иконки для текущей темы
 * @param iconName Название иконки/технологии
 * @param theme Текущая тема ('light' | 'dark')
 * @returns HEX цвет, оптимизированный для темы
 */
export function getIconColor(iconName: string, theme: ThemeMode = 'dark'): string {
  const normalizedName = iconName.toLowerCase().replace(/[^a-z0-9]/g, '');

  const palette = theme === 'light' ? LIGHT_THEME_ICON_COLORS : DARK_THEME_ICON_COLORS;

  // Ищем точное совпадение
  if (palette[normalizedName]) {
    return palette[normalizedName];
  }

  // Ищем частичное совпадение
  for (const [key, value] of Object.entries(palette)) {
    if (key.includes(normalizedName) || normalizedName.includes(key)) {
      return value;
    }
  }

  // Fallback - нейтральный цвет
  return theme === 'light' ? '#64748B' : '#94A3B8';
}

/**
 * Получить цвет иконки по имени технологии из devicon строки
 * @param deviconString Строка вида "devicon:javascript-plain colored" или "lucide:File"
 * @param theme Текущая тема ('light' | 'dark')
 * @returns HEX цвет, оптимизированный для темы
 */
export function getIconColorFromDevicon(deviconString: string, theme: ThemeMode = 'dark'): string {
  // Парсим строку devicon
  if (deviconString.startsWith('devicon:')) {
    // devicon:javascript-plain colored -> javascript
    const parts = deviconString.replace('devicon:', '').split('-');
    const techName = parts[0];
    return getIconColor(techName, theme);
  }

  if (deviconString.startsWith('lucide:')) {
    // lucide:File -> File
    const iconName = deviconString.replace('lucide:', '');
    return getIconColor(iconName, theme);
  }

  // Fallback
  return getIconColor(deviconString, theme);
}

/**
 * Применить CSS цвет к иконке
 * @param iconElement DOM элемент иконки
 * @param iconName Название иконки
 * @param theme Текущая тема
 */
export function applyIconColor(
  iconElement: HTMLElement,
  iconName: string,
  theme: ThemeMode = 'dark'
): void {
  const color = getIconColor(iconName, theme);
  iconElement.style.color = color;
  iconElement.style.fill = color;
}
